CREATE PROCEDURE GET_USERS
RETURNS (
    NAME DM_NAME
)
AS
BEGIN
  SUSPEND;
END;

ALTER PROCEDURE GET_USERS
RETURNS (
    NAME DM_NAME
)
AS
BEGIN
    FOR
        SELECT USERS.NAME
        FROM USERS
        INTO :NAME
    DO SUSPEND;
END;

CREATE PROCEDURE GET_USER_COUNT
RETURNS (
    USER_COUNT INTEGER
)
AS
BEGIN
  SUSPEND;
END;

ALTER PROCEDURE GET_USER_COUNT
RETURNS (
    USER_COUNT INTEGER
)
AS
BEGIN
    SELECT COUNT(*)
    FROM USERS
    INTO :USER_COUNT;
  SUSPEND;
END;

